//Right shift assignment operator ">>" moved the specified number of bits to the right & assign that result to variable
// Excess bit shifted off to the right are discarded and the copies of the leftmost bits are shifted from the left.
let a=4;
console.log(a>>2);

// the binary value for 4 is 000100 if we shift 2 bits right then it will becomes 000001
//  thats the value for binary 1 thats why we get 1.